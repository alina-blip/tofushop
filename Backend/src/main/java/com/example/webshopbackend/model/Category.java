package com.example.webshopbackend.model;
/**
 * Category Enum
 *
 * This enum represents the categories of products available in the webshop. It defines the types of products that can be sold, including ORIGINAL, PRINT, and STICKER.
 */
public enum Category {
    ORIGINAL, // Represents an original product
    PRINT,    // Represents a print of an original product
    STICKER   // Represents a sticker product
}
